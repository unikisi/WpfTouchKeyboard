namespace WpfTouchKeyboard.Settings
{
    /// <summary>
    /// Specifies the type of virtual keyboard to display.
    /// </summary>
    public enum KeyboardType
    {
        /// <summary>
        /// Numeric keyboard only (0-9 and basic controls).
        /// Suitable for numeric input fields.
        /// </summary>
        Number,

        /// <summary>
        /// Full keyboard including letters, numbers, and symbols.
        /// Suitable for general text input.
        /// </summary>
        All
    }
}
